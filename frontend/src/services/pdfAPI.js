import pdfApiClient from './pdfAPIClient';

/**
 * Service pour g√©rer les exports PDF
 */
const pdfAPI = {
  /**
   * T√©l√©charger un re√ßu de transaction
   */
  downloadReceipt: async (transactionId) => {
    try {
      const response = await pdfApiClient.get(`/pdf/transactions/${transactionId}/receipt`);
      
      // Cr√©er un lien de t√©l√©chargement
      const url = window.URL.createObjectURL(response.data);
      const link = document.createElement('a');
      link.href = url;
      link.setAttribute('download', `Recu_Transaction_${transactionId}.pdf`);
      document.body.appendChild(link);
      link.click();
      link.remove();
      window.URL.revokeObjectURL(url);
      
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur t√©l√©chargement re√ßu:', error);
      throw error.response?.data || { message: 'Erreur lors du t√©l√©chargement du re√ßu' };
    }
  },
  
  /**
   * Exporter la liste des transactions
   */
  exportTransactionsList: async (filters = {}) => {
    try {
      // Construire les param√®tres de requ√™te
      const params = new URLSearchParams();
      
      if (filters.status) params.append('status', filters.status);
      if (filters.type) params.append('type', filters.type);
      if (filters.category) params.append('category_id', filters.category);
      if (filters.dateFrom) params.append('date_from', filters.dateFrom);
      if (filters.dateTo) params.append('date_to', filters.dateTo);
      if (filters.search) params.append('search', filters.search);
      if (filters.amountMin) params.append('amount_min', filters.amountMin);
      if (filters.amountMax) params.append('amount_max', filters.amountMax);
      
      const response = await pdfApiClient.get(`/pdf/transactions/export?${params.toString()}`);
      
      // T√©l√©charger le fichier
      const url = window.URL.createObjectURL(response.data);
      const link = document.createElement('a');
      link.href = url;
      
      const today = new Date().toISOString().split('T')[0];
      link.setAttribute('download', `Transactions_${today}.pdf`);
      
      document.body.appendChild(link);
      link.click();
      link.remove();
      window.URL.revokeObjectURL(url);
      
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur export liste:', error);
      throw error.response?.data || { message: 'Erreur lors de l\'export' };
    }
  },
  
  /**
   * G√©n√©rer un rapport financier
   */
  downloadFinancialReport: async (options = {}) => {
    try {
      console.log('üìÑ G√©n√©ration rapport avec options:', options);
      
      const params = new URLSearchParams();
      
      if (options.year) params.append('year', options.year);
      if (options.month) params.append('month', options.month);
      if (options.start_date) params.append('start_date', options.start_date);
      if (options.end_date) params.append('end_date', options.end_date);
      
      const response = await pdfApiClient.get(`/pdf/report/financial?${params.toString()}`);
      
      console.log('‚úÖ R√©ponse re√ßue, taille:', response.data.size);
      
      // T√©l√©charger le fichier
      const url = window.URL.createObjectURL(response.data);
      const link = document.createElement('a');
      link.href = url;
      
      let filename = 'Rapport_Financier';
      if (options.year && options.month) {
        filename += `_${options.month}_${options.year}`;
      } else if (options.year) {
        filename += `_${options.year}`;
      }
      filename += '.pdf';
      
      link.setAttribute('download', filename);
      document.body.appendChild(link);
      link.click();
      link.remove();
      window.URL.revokeObjectURL(url);
      
      console.log('‚úÖ T√©l√©chargement r√©ussi:', filename);
      
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur rapport financier:', error);
      throw error.response?.data || { message: 'Erreur lors de la g√©n√©ration du rapport' };
    }
  },
  
  /**
   * T√©l√©charger le relev√© d'un membre
   */
  downloadMemberStatement: async (memberId) => {
    try {
      const response = await pdfApiClient.get(`/pdf/members/${memberId}/statement`);
      
      // T√©l√©charger le fichier
      const url = window.URL.createObjectURL(response.data);
      const link = document.createElement('a');
      link.href = url;
      link.setAttribute('download', `Releve_Membre_${memberId}.pdf`);
      document.body.appendChild(link);
      link.click();
      link.remove();
      window.URL.revokeObjectURL(url);
      
      return { success: true };
    } catch (error) {
      console.error('‚ùå Erreur relev√© membre:', error);
      throw error.response?.data || { message: 'Erreur lors du t√©l√©chargement' };
    }
  },
  
  /**
   * V√©rifier l'√©tat des assets PDF
   * Note: Cette requ√™te retourne du JSON, pas un blob
   */
  checkAssets: async () => {
    try {
      // Pour cette requ√™te, on utilise l'API normale car elle retourne du JSON
      const { default: api } = await import('./api');
      const response = await api.get('/pdf/check-assets');
      return response.data;
    } catch (error) {
      console.error('‚ùå Erreur v√©rification assets:', error);
      throw error.response?.data || { message: 'Erreur lors de la v√©rification' };
    }
  }
};

export default pdfAPI;